/**
 * Represents the different types of products
 */
export enum ProductType {
    /**
     * Represents a product type classified as "Normal".
     * A normal product typically refers to a standard item sold individually
     */
    Normal = 0,

    /**
     * Represents a product type categorized as a bundle.
     * A bundle typically refers to a collection of individual products or items grouped together
     * as a single unit, often sold at a combined or discounted
     */
    Bundle = 1,

    /**
     * Specifies a product type classified as a "Pack".
     * A pack typically refers to a collection of items or products grouped together
     * Like a pack of beers, a pack of cigarettes, etc.
     */
    Pack = 2,
}
