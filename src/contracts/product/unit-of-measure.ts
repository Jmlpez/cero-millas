/**
 * Represents the different units of measure that can be used for products
 */
export enum UnitOfMeasure {
    /**
     * Single unit (piece, item, etc.)
     */
    Unit = 1,

    /**
     * Pounds (lb)
     */
    Lbs = 2,

    /**
     * Ounces (oz)
     */
    Oz = 3,

    /**
     * Kilograms (kg)
     */
    Kg = 4,

    /**
     * Grams (g)
     */
    Grams = 5,

    /**
     * Liters (L)
     */
    Liters = 6,

    /**
     * Milliliters (mL)
     */
    Milliliters = 7,

    /**
     * Gallons (gal)
     */
    Gallons = 8,

    /**
     * Feet (ft)
     */
    Feet = 9,

    /**
     * Inches (in)
     */
    Inches = 10,

    /**
     * Meters (m)
     */
    Meters = 11,

    /**
     * Centimeters (cm)
     */
    Centimeters = 12,

    /**
     * Box
     */
    Box = 13,

    /**
     * Case
     */
    Case = 14,

    /**
     * Pack
     */
    Pack = 15,

    /**
     * Dozen
     */
    Dozen = 16
}
